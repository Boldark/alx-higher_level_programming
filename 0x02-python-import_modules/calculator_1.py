#!/usr/bin/python3
# ffunction for addition of 2 arguments a and b
def add(a, b):
    return (a + b)


# function for subtraction of 2 arguments a and b
def sub(a, b):
    return (a - b)


# function for division of 2 arguments a and b
def div(a, b):
    return (a / b)


# function for multiplication of 2 arguments a and b
def mul(a, b):
    return (a * b)
